/**
 * Convert emoji shortcode to Unicode character
 * @param shortcode The emoji shortcode (e.g., 'smile', 'heart')
 * @returns The Unicode emoji character, or null if not found
 */
export function shortcodeToEmoji(shortcode: string): string | null {
  // Normalize shortcode: remove colons and convert to lowercase
  const normalized = shortcode.replace(/^:|:$/g, '').toLowerCase().replace(/-/g, '_')
  return SHORTCODE_TO_EMOJI[normalized] ?? null
}

/**
 * Check if an emoji shortcode has a Unicode mapping
 * @param shortcode The emoji shortcode
 * @returns true if the shortcode has a Unicode mapping
 */
export function hasEmojiMapping(shortcode: string): boolean {
  return shortcodeToEmoji(shortcode) !== null
}

/**
 * Get all available shortcodes
 * @returns Array of all supported shortcodes
 */
export function getAllShortcodes(): string[] {
  return Object.keys(SHORTCODE_TO_EMOJI)
}
